#include <iostream>

using namespace std;

int main()
{
    // << : Operador de insercion
    /*
        cout << "Hello world!" << endl;
        Con << a cout le insertamos el mensaje:
        Hello World!
        Posteriormente le insertamos endl, que es
        el salto de linea.
        Terminando la instrucciòn con ;
    */
    cout << "Hello world 1!" << endl;
    cout << "Hello world 2!" << endl;
    cout << "Hello world 1!";
    cout << "Hello world 2!" << endl;
    cout << "Hello world 1!\n";
    cout << "Hello world 2!" << endl;
    return 0;
}
