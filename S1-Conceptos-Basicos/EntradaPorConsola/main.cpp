#include <iostream>

using namespace std;

int main()
{
    /**ENTRADA POR CONSOLA**/
    /** cin : Console Input **/
    /** >> : Operador de Extracción **/
    int numero;
    cout << "Ingrese un numero: ";
    cin >> numero;
    cout << "El valor ingresado es: " << numero << endl;
    return 0;
}
