#include <iostream>

using namespace std;

int main()
{
    /** DIVISIÓN ENTERA Y DIVISIÓN REAL **/
    /**
        La división entera siempre se da entre dos valores
        enteros, el resultado siempre es entero.
    **/
    cout << 7/3 << endl;
    /**
        La división real se da cuando al menos uno de los
        dos valores es real (float o double).
    **/
    cout << 7.0/3 << endl;
    return 0;
}
