#include <iostream>

using namespace std;

int main()
{
    /**
        OPERADORES MATEMÁTICOS/ARITMÉTICOS
        + : SUMA
        - : RESTA
        * : MULTIPLICACIÓN
        / : DIVISIÓN
        % : MÓDULO
    **/
    int suma,resta,mult,division,modulo;
    int numero1 = 10;
    int numero2 = 3;
    suma = numero1 + numero2;
    resta = numero2 - numero1;
    mult = numero1 * numero2;
    division = numero1 / numero2;
    modulo = numero1 % numero2;
    cout << suma << endl;
    cout << resta << endl;
    cout << mult << endl;
    cout << division << endl;
    cout << modulo << endl;
    return 0;
}
